using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace XingAo.Software.UserCenter.Account
{
    /// <summary>
    /// 积分记录实体接口
    /// </summary>
    public interface IPointRecord
    {
        /// <summary>
        /// 编号
        /// </summary>
        public string Id { get; set; }
        /// <summary>
        /// 获得或者使用的积分数
        /// </summary>
        public int Point { get; set; }
        /// <summary>
        /// 用途（获得或者使用的方式）
        /// </summary>
        public string Usage { get; set; }
        /// <summary>
        /// 获得或者使用的时间
        /// </summary>
        public DateTime Time { get; set; }
        /// <summary>
        /// 描述（用于对积分获得或者使用的详细描述，比用途更详细）
        /// </summary>
        public string Descrption { get; set; }
    }
}
